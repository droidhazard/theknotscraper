function findDuplicates(array) {
  const duplicates = [];
  const seen = new Map();

  array.forEach((item) => {
    const key = typeof item === "object" ? JSON.stringify(item) : item;

    if (seen.has(key)) {
      if (seen.get(key) === 1) {
        duplicates.push(item);
      }
      seen.set(key, seen.get(key) + 1);
    } else {
      seen.set(key, 1);
    }
  });

  return duplicates;
}

